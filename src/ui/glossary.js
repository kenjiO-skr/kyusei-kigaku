// 用語スパンを生成。data-term をキーに吹き出しを出す。
export function term(label, key = label) {
  const k = GLOSSARY[key] ? key : label;
  if (!GLOSSARY[k]) return label; // 未登録ならただのテキスト
  return `<span class="term" data-term="${k}">${label}</span>`;
}
